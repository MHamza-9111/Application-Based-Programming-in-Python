# Session 3 Arithmetic Operations
print(2 + 3)  # Addition: prints the result of 2 + 3
print (2 - 3) # Subtraction: prints the result of 2 - 3
print (2 * 3) # Multiplication: prints the result of 2 * 3
print (25 / 5) # Division: prints the result of 25 / 5
print (25 // 5) # Floor Division: prints the result of 25 // 5

# Session 4 Logical Operations
print (2 > 3)  # Greater than: prints False since 2 is not greater than 3
print (2 < 3)  # Less than: prints True since 2 is less than 3
print (2 == 2) # Equality: prints True since 2 is equal to 2
print(2 != 3) # Not equal: prints True since 2 is not equal to 3
print (2 >= 3) # Greater than or equal to: prints False since 2 is not greater than or equal to 3

